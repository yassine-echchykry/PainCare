// Pain_Bean.java
package PainCare.Beans;



public class Pain_Bean {
    private int id;
    private int user_id;
    private int pain_level;
    private String locations;
    private String symptoms;
    private String worse_pain;
    private String feelings;
    
    
    //
    public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getPain_level() {
		return pain_level;
	}
	public void setPain_level(int pain_level) {
		this.pain_level = pain_level;
	}
	public String getLocations() {
		return locations;
	}
	public void setLocations(String locations) {
		this.locations = locations;
	}
	public String getSymptoms() {
		return symptoms;
	}
	public void setSymptoms(String symptoms) {
		this.symptoms = symptoms;
	}
	public String getWorse_pain() {
		return worse_pain;
	}
	public void setWorse_pain(String worse_pain) {
		this.worse_pain = worse_pain;
	}
	public String getFeelings() {
		return feelings;
	}
	public void setFeelings(String feelings) {
		this.feelings = feelings;
	}
	

    // Getters and Setters
}
